static int _brd_add(int argc, char **argv, tman_ctx_t * ctx)
{
    char c;
    tman_arg_t args;
    const char *errfmt = "cannot add board '%s': %s";
    int i, quiet, showhelp, status;
    tman_opt_t opt = {
        .brd_switch = TRUE,
    };

    showhelp = quiet = FALSE;
    args.prj = args.brd = args.task = NULL;
    while ((c = getopt(argc, argv, ":hnp:q")) != -1) {
        switch (c) {
        case 'h':
            showhelp = TRUE;
            break;
        case 'n':
            opt.brd_switch = FALSE;
            break;
        case 'p':
            args.prj = optarg;
            break;
        case 'q':
            quiet = TRUE;
            break;
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if (showhelp)
        return help_usage("brd-add");

    if (optind == argc)
        return elog(1, "board name required");

    for (i = optind; i < argc; ++i) {
        args.brd = argv[i];

        if (generate_units(ctx, args.brd)) {
            if (quiet == FALSE)
                elog(1, errfmt, args.prj, "unit generation failed");
            continue;
        } else if ((status = tman_brd_add(ctx, &args, &opt)) != LIBTMAN_OK) {
            if (quiet == FALSE)
                elog(1, errfmt, argv[i], tman_strerror());
            tman_unit_free(ctx, &args, NULL);
            continue;
        }
        // TODO: add hooks
        tman_unit_free(ctx, &args, NULL);
    }
    return status == LIBTMAN_OK ? tman_pwd_board() : 1;
}

static int _brd_del(int argc, char **argv, tman_ctx_t * ctx)
{
    char c;
    tman_arg_t args;
    const char *errfmt = "cannot delete: %s";
    int i, choice, quiet, showhelp, showprompt, status;
    tman_opt_t opt;

    showprompt = TRUE;
    choice = quiet = showhelp = FALSE;
    args.prj = args.brd = args.task = NULL;
    while ((c = getopt(argc, argv, ":hnq")) != -1) {
        switch (c) {
        case 'h':
            showhelp = TRUE;
            break;
        case 'n':
            showprompt = FALSE;
            break;
        case 'q':
            quiet = TRUE;
            break;
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if (showhelp)
        return help_usage("brd-del");

    i = optind;
    do {
        args.brd = argv[i];

        if (showprompt) {
            /* TODO: show board name.  */
            printf("Are you sure to delete board(s)? [y/N] ");
            if ((choice = getchar()) != 'y' && choice != 'Y')
                continue;
        }

        if ((status = tman_brd_del(ctx, &args, &opt)) != LIBTMAN_OK) {
            if (quiet == FALSE)
                elog(status, errfmt, argv[i], tman_strerror());
        }
    } while (++i < argc);

    // TODO: update current directory if current prj got deleted.
    return status == LIBTMAN_OK ? tman_pwd_board() : status;
}

