int tman_cli_board(int argc, char **argv, tman_ctx_t * ctx)
{
    char *cmd;

    if ((cmd = argv[1]) == NULL)
        return elog(1, "brd command is missing");

    for (int i = 0; i < ARRAY_SIZE(brdcmds); ++i)
        if (strncmp(cmd, brdcmds[i].name, CMDSIZ) == 0)
            return brdcmds[i].func(argc - 1, argv + 1, ctx);

    return elog(1, "'%s': no such brd subcommand", cmd);
}
