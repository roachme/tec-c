static int _board_set(int argc, char **argv, tec_ctx_t * ctx)
{
    tec_arg_t args;
    int atleast_one_key_set;
    int c, i, quiet, showhelp;
    const char *errfmt = "could not set board unit value '%s': %s";

    quiet = showhelp = false;
    atleast_one_key_set = false;
    args.project = args.board = args.taskid = NULL;
    while ((c = getopt(argc, argv, ":d:hq")) != -1) {
        switch (c) {
        case 'd':
            if (valid_desc(optarg) == false) {
                elog(1, "invalid description '%s'", optarg);
                help_usage("board-set");
                return 1;
            }
            atleast_one_key_set = true;
            ctx->units = tec_unit_add(ctx->units, "desc", optarg);
            break;
        case 'h':
            showhelp = true;
            break;
        case 'q':
            quiet = true;
            break;
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if (showhelp)
        return help_usage("board-set");
    if (atleast_one_key_set == false) {
        elog(1, "gotta supply one of the options");
        help_usage("board-set");
        return 1;
    }

    i = optind;
    do {
        int status;
        args.board = argv[i];

        if ((status = tec_board_set(teccfg.base.task, &args, ctx))) {
            if (quiet == false)
                elog(status, errfmt, argv[i], tec_strerror(status));
        }
    } while (++i < argc);

    ctx->units = tec_unit_free(ctx->units);
    return 0;
}

static int _board_show(int argc, char **argv, tec_ctx_t * ctx)
{
    tec_arg_t args;
    int c, i, quiet, showhelp, status;
    struct tec_unit *units, *unitpgn;
    const char *errfmt = "cannot show project units '%s': %s";

    units = unitpgn = NULL;
    quiet = showhelp = false;
    args.project = args.board = args.taskid = NULL;
    while ((c = getopt(argc, argv, ":hq")) != -1) {
        switch (c) {
        case 'h':
            showhelp = true;
            break;
        case 'q':
            quiet = true;
            break;
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if (showhelp)
        return help_usage("board-show");

    i = optind;
    do {
        args.board = argv[i];
        if ((status = tec_board_get(teccfg.base.task, &args, ctx)) != LIBTEC_OK) {
            if (quiet == false)
                elog(status, errfmt, argv[i], tec_strerror(status));
            continue;
        }

        printf("%-7s : %s\n", "board", args.board);
        for (units = ctx->units; units; units = units->next)
            printf("%-7s : %s\n", units->key, units->val);

        // TODO: add plugin output
    } while (++i < argc);

    return status;
}

static int _board_sync(int argc, char **argv, tec_ctx_t * ctx)
{
    tec_arg_t args;
    int c, i, quiet, showhelp, status;
    const char *errfmt = "cannot switch to '%s': %s";
    int switch_toggle, switch_dir;

    quiet = showhelp = false;
    switch_toggle = switch_dir = true;
    args.project = args.board = args.taskid = NULL;
    while ((c = getopt(argc, argv, ":hnp:qN")) != -1) {
        switch (c) {
        case 'h':
            showhelp = true;
            break;
        case 'n':
            switch_toggle = false;
            break;
        case 'p':
            args.project = optarg;
            break;
        case 'q':
            quiet = true;
            break;
        case 'N':
            switch_dir = false;
            switch_toggle = false;
            break;
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if (showhelp)
        return help_usage("board-sync");

    if ((status = check_arg_project(&args, errfmt, quiet)))
        return status;

    i = optind;
    do {
        args.board = argv[i];
        if ((status = check_arg_board(&args, errfmt, quiet)))
            return status;
    } while (++i < argc);

    if (status == LIBTEC_OK && switch_toggle == true) {
        if (toggle_board_set_curr(teccfg.base.task, &args) && quiet == false)
            status = elog(1, "could not update board toggles");
    }

    return status == LIBTEC_OK && switch_dir ? tec_pwd_board(&args) : status;
}

static const builtin_t board_commands[] = {
    {.name = "add",.func = &_board_add},
    {.name = "del",.func = &_board_del},
    {.name = "list",.func = &_board_list},
    {.name = "prev",.func = &_board_prev},
    {.name = "move",.func = &_board_move},
    {.name = "set",.func = &_board_set},
    {.name = "show",.func = &_board_show},
    {.name = "sync",.func = &_board_sync},
};

int tec_cli_board(int argc, char **argv, tec_ctx_t * ctx)
{
    char *cmd = argv[1] != NULL ? argv[1] : "list";

    for (int i = 0; i < ARRAY_SIZE(board_commands); ++i)
        if (strcmp(cmd, board_commands[i].name) == 0)
            return board_commands[i].func(argc - 1, argv + 1, ctx);

    return elog(1, "'%s': no such board subcommand", cmd);
}
