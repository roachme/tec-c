static int valid_desc(const char *val)
{
    if (!isalnum(*val++))
        return FALSE;
    for (; *val; ++val)
        if (!(isalnum(*val) || isspace(*val) || *val == '_' || *val == '-'))
            return FALSE;
    return isalnum(*--val) != 0;
}

static int generate_units(tman_ctx_t * ctx, char *brd)
{
    struct tman_unit *units = NULL;
    char desc[100] = "autogenerated desciption for board ";

    strcat(desc, brd);
    units = tman_unit_add(units, "desc", desc);
    ctx->units = units;
    return 0;
}

