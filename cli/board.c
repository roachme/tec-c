static int _board_add(int argc, char **argv, tec_ctx_t * ctx)
{
    char c;
    tec_arg_t args;
    const char *errfmt = "cannot add board '%s': %s";
    int i, quiet, showhelp, status;

    status = LIBTEC_OK;
    showhelp = quiet = false;
    args.project = args.board = args.taskid = NULL;
    while ((c = getopt(argc, argv, ":hnp:q")) != -1) {
        switch (c) {
        case 'h':
            showhelp = true;
            break;
        case 'n':
            //opt.board_switch = false;
            break;
        case 'p':
            args.project = optarg;
            break;
        case 'q':
            quiet = true;
            break;
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if (showhelp)
        return help_usage("board-add");

    if (optind == argc)
        return elog(1, "board name required");
    else if ((ctx->column = generate_column("todo")) == NULL) {
        if (quiet == false)
            elog(1, "could not generate column");
        return 1;
    }

    if ((status = toggle_project_get_curr(teccfg.base.task, &args))) {
        return status;
    }

    for (i = optind; i < argc; ++i) {
        args.board = argv[i];

        if (generate_units(ctx, args.board)) {
            if (quiet == false)
                elog(1, errfmt, args.project, "unit generation failed");
            continue;
        }
        if ((status = tec_board_add(teccfg.base.task, &args, ctx))) {
            if (quiet == false)
                elog(1, errfmt, argv[i], tec_strerror(status));
            ctx->units = tec_unit_free(ctx->units);
            continue;
        }
        // TODO: add hooks
        ctx->units = tec_unit_free(ctx->units);
    }

    ctx->column = tec_unit_free(ctx->column);
    //return status == LIBTEC_OK ? tec_pwd_board(&args) : 1;
    return 0;
}

static int _board_del(int argc, char **argv, tec_ctx_t * ctx)
{
    char c;
    tec_arg_t args;
    const char *errfmt = "cannot delete: %s";
    int i, choice, quiet, showhelp, showprompt, status;

    showprompt = true;
    status = LIBTEC_OK;
    choice = quiet = showhelp = false;
    args.project = args.board = args.taskid = NULL;
    while ((c = getopt(argc, argv, ":hnq")) != -1) {
        switch (c) {
        case 'h':
            showhelp = true;
            break;
        case 'n':
            showprompt = false;
            break;
        case 'q':
            quiet = true;
            break;
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if (showhelp)
        return help_usage("board-del");

    i = optind;
    do {
        args.board = argv[i];

        if (showprompt) {
            /* TODO: show board name.  */
            printf("Are you sure to delete board(s)? [y/N] ");
            if ((choice = getchar()) != 'y' && choice != 'Y')
                continue;
        }

        if ((status = tec_board_del(teccfg.base.task, &args, ctx))) {
            if (quiet == false)
                elog(status, errfmt, argv[i], tec_strerror(status));
        }
    } while (++i < argc);

    // TODO: update current directory if current project got deleted.
    return status == LIBTEC_OK ? tec_pwd_board(&args) : status;
}

