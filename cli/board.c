static int _board_list(int argc, char **argv, tec_ctx_t * ctx)
{
    tec_arg_t args;
    int c, i, quiet, status;
    const char *errfmt = "cannot list board(s) '%s': %s";

    quiet = false;
    while ((c = getopt(argc, argv, ":q")) != -1) {
        switch (c) {
        case 'q':
            quiet = true;
            break;
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if ((status = toggle_project_get_curr(teccfg.base.task, &args))) {
        if (quiet == false)
            elog(status, errfmt, "NOCURR", "no current project");
        return status;
    } else if ((status = tec_project_valid(teccfg.base.task, &args))) {
        if (quiet == false)
            elog(status, errfmt, args.board, tec_strerror(status));
        return status;
    }

    i = optind;
    do {
        args.board = argv[i];

        if (check_arg_board(&args, errfmt, quiet))
            continue;
        if ((status = tec_board_list(teccfg.base.task, &args, ctx))) {
            if (quiet == false)
                elog(status, errfmt, args.board, tec_strerror(status));
            continue;
        }

        for (tec_list_t * obj = ctx->list; obj != NULL; obj = obj->next) {
            LIST_BOARD_UNITS("mark", obj->name, "some board description");
        }
        ctx->list = tec_list_free(ctx->list);
    } while (++i < argc);
    return status;
}

