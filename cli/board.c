static int valid_desc(const char *val)
{
    if (!isalnum(*val++))
        return false;
    for (; *val; ++val)
        if (!(isalnum(*val) || isspace(*val) || *val == '_' || *val == '-'))
            return false;
    return isalnum(*--val) != 0;
}

static int generate_units(tec_ctx_t * ctx, char *board)
{
    struct tec_unit *units = NULL;
    char desc[100] = "autogenerated desciption for board ";

    strcat(desc, board);
    units = tec_unit_add(units, "desc", desc);
    ctx->units = units;
    return 0;
}

