static int _project_rename(int argc, char **argv, tman_ctx_t * ctx)
{
    int c, quiet, showhelp, status;
    tman_arg_t src, dst;

    quiet = showhelp = FALSE;
    src.project = src.board = src.taskid = NULL;
    dst.project = dst.board = dst.taskid = NULL;
    while ((c = getopt(argc, argv, ":hq")) != -1) {
        switch (c) {
        case 'h':
            showhelp = TRUE;
            break;
        case 'q':
            quiet = TRUE;
            break;
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if (showhelp)
        return help_usage("prj-rename");

    if (argc - optind != 2)
        return elog(1, "source or destination project name missing");

    src.project = argv[optind];
    dst.project = argv[optind + 1];

    /* TODO: trigger hooks if any */

    if ((status = tman_project_rename(NULL, &src, &dst))) {
        if (quiet == FALSE)
            elog(status, "could not rename project: %s", tman_strerror(status));
    }
    return tman_pwd_project();
}

static int _project_set(int argc, char **argv, tman_ctx_t * ctx)
{
    tman_arg_t args;
    int atleast_one_key_set;
    int c, i, quiet, showhelp;
    const char *errfmt = "could not set project unit value '%s': %s";

    quiet = showhelp = FALSE;
    atleast_one_key_set = FALSE;
    args.project = args.board = args.taskid = NULL;
    while ((c = getopt(argc, argv, ":b:d:hq")) != -1) {
        switch (c) {
        case 'b':
            args.board = optarg;
            break;
        case 'd':
            if (valid_desc(optarg) == FALSE) {
                elog(1, "invalid description '%s'", optarg);
                help_usage("prj-set");
                return 1;
            }
            atleast_one_key_set = TRUE;
            ctx->units = tman_unit_add(ctx->units, "desc", optarg);
            break;
        case 'h':
            showhelp = TRUE;
            break;
        case 'q':
            quiet = TRUE;
            break;
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if (showhelp)
        return help_usage("project-set");
    if (atleast_one_key_set == FALSE) {
        elog(1, "gotta supply one of the options");
        help_usage("project-set");
        return 1;
    }

    i = optind;
    do {
        int status;
        args.project = argv[i];

        if ((status = tman_project_set(ctx, &args)) != LIBTMAN_OK) {
            if (quiet == FALSE)
                elog(status, errfmt, argv[i], tman_strerror(status));
        }
    } while (++i < argc);

    ctx->units = tman_unit_free(ctx->units);
    return 0;
}

