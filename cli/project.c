static int generate_units(tman_ctx_t * ctx, char *prj)
{
    struct tman_unit *units = NULL;
    char desc[100] = "autogenerated desciption for project ";

    strcat(desc, prj);
    units = tman_unit_add(units, "desc", desc);
    ctx->units = units;
    return 0;
}

static int valid_desc(const char *val)
{
    if (!isalnum(*val++))
        return FALSE;
    for (; *val; ++val)
        if (!(isalnum(*val) || isspace(*val) || *val == '_' || *val == '-'))
            return FALSE;
    return isalnum(*--val) != 0;
}

