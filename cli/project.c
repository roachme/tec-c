static int _project_add(int argc, char **argv, tman_ctx_t * ctx)
{
    char c;
    tman_arg_t args;
    const char *errfmt = "cannot add project '%s': %s";
    int i, quiet, showhelp, status;

    status = LIBTMAN_OK;
    showhelp = quiet = FALSE;
    args.project = args.board = args.taskid = NULL;
    while ((c = getopt(argc, argv, ":b:hnqB")) != -1) {
        switch (c) {
        case 'b':
            args.board = optarg;
            break;
        case 'h':
            showhelp = TRUE;
            break;
        case 'n':
            //opt.prj_switch = FALSE;
            break;
        case 'q':
            quiet = TRUE;
            break;
        case 'B':
            return elog(1, "option `-B': under development");
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if (showhelp)
        return help_usage("project-add");

    if (optind == argc)
        return elog(1, "project name required");
    else if ((ctx->column = generate_column("todo")) == NULL) {
        if (quiet == FALSE)
            elog(1, "could not generate column");
        return 1;
    }

    /* Set default board name to create.  */
    if (args.board == NULL)
        args.board = "brd-000";

    for (i = optind; i < argc; ++i) {
        args.project = argv[i];

        if (generate_units(ctx, args.project)) {
            if (quiet == FALSE)
                elog(1, errfmt, args.project, "unit generation failed");
            continue;
        } else if ((status = tman_project_add(ctx, &args)) != LIBTMAN_OK) {
            if (quiet == FALSE)
                elog(1, errfmt, argv[i], tman_strerror(status));
            continue;
        }
        // TODO: add support to add default board when project gets created
        // TODO: create default board if option to use boards is set in config
        ctx->units = tman_unit_free(ctx->units);
    }
    ctx->column = tman_unit_free(ctx->column);

    //return opt.prj_switch && status == LIBTMAN_OK ? tman_pwd_project() : status;
    //return status == LIBTMAN_OK ? tman_pwd_project() : status;
    return 0;
}

static int _project_del(int argc, char **argv, tman_ctx_t * ctx)
{
    char c;
    tman_arg_t args;
    const char *errfmt = "cannot delete project: %s";
    int i, choice, quiet, showhelp, showprompt, status;

    showprompt = TRUE;
    choice = quiet = showhelp = FALSE;
    args.project = args.board = args.taskid = NULL;
    while ((c = getopt(argc, argv, ":b:hnq")) != -1) {
        switch (c) {
        case 'b':
            args.board = optarg;
            break;
        case 'h':
            showhelp = TRUE;
            break;
        case 'n':
            showprompt = FALSE;
            break;
        case 'q':
            quiet = TRUE;
            break;
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if (showhelp)
        return help_usage("project-del");

    if (showprompt) {
        printf("Are you sure to delete projects(s)? [y/N] ");
        choice = getchar();
        if (choice != 'y' && choice != 'Y') {
            return 0;
        }
    }

    i = optind;
    do {
        args.project = argv[i];
        if ((status = tman_project_del(ctx, &args)) != LIBTMAN_OK) {
            if (quiet == FALSE)
                elog(status, errfmt, argv[i], tman_strerror(status));
        }
    } while (++i < argc);

    dlog(1, "prj_del: status = %d", status);

    // TODO: update current directory if current prj got deleted.
    return status == LIBTMAN_OK ? tman_pwd_project() : status;
}

static int _project_list(int argc, char **argv, tman_ctx_t * ctx)
{
    int c, quiet, status;

    quiet = FALSE;
    while ((c = getopt(argc, argv, ":q")) != -1) {
        switch (c) {
        case 'q':
            quiet = TRUE;
            break;
        case ':':
            return elog(1, "option `-%c' requires an argument", optopt);
        default:
            return elog(1, "invalid option `-%c'", optopt);
        }
    }

    if ((status = tman_project_list(ctx)) != LIBTMAN_OK) {
        if (quiet == FALSE) {
            const char *errfmt = "cannot list project(s) '%s': %s";
            elog(status, errfmt, "PRJ", tman_strerror(status));
        }
    }
    return status;
}

